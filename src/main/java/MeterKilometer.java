public class MeterKilometer {

     public float convertMeterToKilometer(int meter) {
         System.out.println();
         float kilometer = meter / 1000.0f;
         return kilometer;
     }

    public int convertKilometerToMeter(int kilometer) {
        System.out.println();
        int meter = kilometer * 1000;
        return meter;
    }

}

public class LiterMilliliter {


    public int convertLiterToMilliliter(float liter) {
        System.out.println();
        int milliliter = (int) (liter * 1000);
        return milliliter;
    }

    public float convertMilliliterToLiter(float milliliter) {
        System.out.println();
        float liter = milliliter / 1000.0f;
        return liter;
    }

}
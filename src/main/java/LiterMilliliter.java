public class LiterMilliliter {


    public int convertLiterToMilliliter(int liter) {
        System.out.println();
        int milliliter = liter * 1000;
        return milliliter;
    }
    public float convertMilliliterToLiter(int milliliter) {
        System.out.println();
        float liter = milliliter / 1000.0f;
        return liter;
    }

}